package me.masstrix.eternalnature.util;

public class SecondsFormat {

    public String format(int seconds) {
        int mins = seconds / 60;
        int sec = seconds % 60;

        if (mins > 99) return "**:**";
        return mins + ":" + String.format("%02d", sec);
    }
}
